"""
Tipos de Dados
"""
string = "Texto"
int = 123456
float = 10.50
bool = True

print(string, type(string), int, type(int), float,  type(float), bool, type(bool))

# string: Nome
print('Antonio Oliveira', type('Antonio Oliveira'))
# int: Idade
print(36, type(36))
# float: Altura
print(1.90, type(1.90))
# bool: Chegar maior idade
print(36 > 18, type(36 > 18))
